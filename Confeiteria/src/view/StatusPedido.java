    public void exibirPedidos(){
        DefaultTableModel modelo = (DefaultTableModel) jTableStatusPedido.getModel();
        modelo.setNumRows(0);
        PedidoDao dao  = new PedidoDao();
        
        for (Pedido p: dao.consultarPedido()){
            modelo.addRow(new Object[]{
            
            p.getPedido_id(),
            p.getStatus_pedido(),
            p.getData_pedido(),
            p.getData_entrega()
            
        });
        }
    }

    private void jButtonExcluirPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirPedidoActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model1 = (DefaultTableModel) jTableStatusPedido.getModel();
        int selecionarLinha = jTableStatusPedido.getSelectedRow();
        
        Pedido pedido= new Pedido();
        String id;
        id = model1.getValueAt(selecionarLinha, 0).toString();
        pedido.setPedido_id(Integer.parseInt(id));
        pedido.setStatus_pedido(model1.getValueAt(selecionarLinha, 1).toString());
        
        
        
        
        PedidoController pedidoControler = new PedidoController();
        
        pedidoControler.excluirPedido(pedido);
        
    }//GEN-LAST:event_jButtonExcluirPedidoActionPerformed

